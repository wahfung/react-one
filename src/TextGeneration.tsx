interface Message {
  id: string;
  content: string;
  sender: "user" | "ai";
  timestamp: Date;
}

// ‰ΩøÁî®ËÆ∞ÂøÜÂåñÊäÄÊúØÁºìÂ≠òÂ∑≤Â§ÑÁêÜÁöÑÊ∂àÊÅØ
const messageCache = new Map<string, React.ReactNode>();

// Ê†πÊçÆÊ∂àÊÅØÂÜÖÂÆπÂíåagentÁ±ªÂûãÂ§ÑÁêÜÊ∂àÊÅØÊ†ºÂºèÂåñ
const formatMessage = (
  content: string,
  agentType: AgentType
): React.ReactNode => {
  // ËÅäÂ§©Ê®°ÂºèÁÆÄÂçïËøîÂõûÊñáÊú¨
  if (agentType !== "codeReview") {
    return content;
  }

  // Ê£ÄÊü•ÁºìÂ≠ò‰∏≠ÊòØÂê¶Â∑≤ÊúâÊ≠§Ê∂àÊÅØÁöÑÊ†ºÂºèÂåñÁªìÊûú
  const cacheKey = `${content}-${agentType}`;
  if (messageCache.has(cacheKey)) {
    return messageCache.get(cacheKey);
  }

  // Â§ÑÁêÜ‰ª£Á†ÅÂÆ°Êü•Ê®°Âºè
  try {
    // Â∞ùËØïËß£Êûê‰ª£Á†ÅÂùóÂíåÈ´ò‰∫ÆÈÉ®ÂàÜ
    const formattedContent: React.ReactNode[] = [];
    let currentIndex = 0;

    // ÁÆÄÂçïÁöÑÊ≠£ÂàôË°®ËææÂºèÂåπÈÖç‰ª£Á†ÅÂùó ```code```
    const codeBlockPattern = /```([^`]+)```/g;
    let match;

    // È¢ÑÂÖàÁºñËØëÊ≠£ÂàôË°®ËææÂºè
    const issuePattern =
      /(‚ùó\s*Issue:|‚ö†Ô∏è\s*Warning:|‚úÖ\s*Good:|üìù\s*Suggestion:)([^‚ùó‚ö†Ô∏è‚úÖüìù]+)/g;

    while ((match = codeBlockPattern.exec(content)) !== null) {
      // Ê∑ªÂä†‰ª£Á†ÅÂùóÂâçÁöÑÊñáÊú¨
      if (match.index > currentIndex) {
        const textBefore = content.slice(currentIndex, match.index);
        formattedContent.push(
          <span key={`text-${currentIndex}`}>{textBefore}</span>
        );
      }

      // Ê∑ªÂä†‰ª£Á†ÅÂùó
      formattedContent.push(
        <div key={`code-${match.index}`} style={styles.codeBlock}>
          {match[1]}
        </div>
      );

      currentIndex = match.index + match[0].length;
    }

    // Ê∑ªÂä†Ââ©‰ΩôÁöÑÊñáÊú¨
    if (currentIndex < content.length) {
      formattedContent.push(
        <span key={`text-${currentIndex}`}>{content.slice(currentIndex)}</span>
      );
    }

    // Â§ÑÁêÜÈ´ò‰∫ÆÈÉ®ÂàÜ - ‰ΩøÁî®ÂáΩÊï∞ÁÆÄÂåñÂ§ÑÁêÜÈÄªËæë
    const processNode = (node: any, index: number): React.ReactNode => {
      // ÂÆâÂÖ®Âú∞Ê£ÄÊü•nodeÊòØÂê¶‰∏∫ReactÂÖÉÁ¥†ÔºåÂπ∂ÂÖ∑Êúâprops.children
      if (
        node &&
        typeof node === "object" &&
        "props" in node &&
        node.props &&
        "children" in node.props
      ) {
        const nodePC = node.props.children;

        // Âè™Â§ÑÁêÜÂ≠óÁ¨¶‰∏≤ÂÜÖÂÆπ
        if (typeof nodePC === "string") {
          const text = nodePC;
          const parts: React.ReactNode[] = [];
          let lastIndex = 0;

          // ÈáçÁΩÆÊ≠£ÂàôË°®ËææÂºè
          issuePattern.lastIndex = 0;

          let highlightMatch;
          while ((highlightMatch = issuePattern.exec(text)) !== null) {
            // Ê∑ªÂä†ÂâçÈù¢ÁöÑÊôÆÈÄöÊñáÊú¨
            if (highlightMatch.index > lastIndex) {
              parts.push(text.slice(lastIndex, highlightMatch.index));
            }

            // Ê∑ªÂä†È´ò‰∫ÆÈÉ®ÂàÜ
            parts.push(
              <div
                key={`highlight-${highlightMatch.index}`}
                style={styles.highlightSection}
              >
                <strong>{highlightMatch[1]}</strong> {highlightMatch[2]}
              </div>
            );

            lastIndex = highlightMatch.index + highlightMatch[0].length;
          }

          // Ê∑ªÂä†Ââ©‰ΩôÁöÑÊñáÊú¨
          if (lastIndex < text.length) {
            parts.push(text.slice(lastIndex));
          }

          return parts.length > 1 ? (
            <span key={`formatted-${index}`}>{parts}</span>
          ) : (
            node
          );
        }
      } else if (typeof node === "string") {
        // Áõ¥Êé•Â§ÑÁêÜÂ≠óÁ¨¶‰∏≤ËäÇÁÇπ
        const text = node;
        const parts: React.ReactNode[] = [];
        let lastIndex = 0;

        // ÈáçÁΩÆÊ≠£ÂàôË°®ËææÂºè
        issuePattern.lastIndex = 0;

        let highlightMatch;
        while ((highlightMatch = issuePattern.exec(text)) !== null) {
          // Ê∑ªÂä†ÂâçÈù¢ÁöÑÊôÆÈÄöÊñáÊú¨
          if (highlightMatch.index > lastIndex) {
            parts.push(text.slice(lastIndex, highlightMatch.index));
          }

          // Ê∑ªÂä†È´ò‰∫ÆÈÉ®ÂàÜ
          parts.push(
            <div
              key={`highlight-${highlightMatch.index}`}
              style={styles.highlightSection}
            >
              <strong>{highlightMatch[1]}</strong> {highlightMatch[2]}
            </div>
          );

          lastIndex = highlightMatch.index + highlightMatch[0].length;
        }

        // Ê∑ªÂä†Ââ©‰ΩôÁöÑÊñáÊú¨
        if (lastIndex < text.length) {
          parts.push(text.slice(lastIndex));
        }

        return parts.length > 1 ? (
          <span key={`string-${index}`}>{parts}</span>
        ) : (
          node
        );
      }

      return node;
    };

    // Â§ÑÁêÜÊâÄÊúâËäÇÁÇπ
    const finalContent = formattedContent.map(processNode);

    // ÁºìÂ≠òÁªìÊûú
    const result = <>{finalContent}</>;
    messageCache.set(cacheKey, result);

    // ÈôêÂà∂ÁºìÂ≠òÂ§ßÂ∞èÔºåÈÅøÂÖçÂÜÖÂ≠òÊ≥ÑÊºè
    if (messageCache.size > 100) {
      const firstKey = messageCache.keys().next().value;
      messageCache.delete(firstKey);
    }

    return result;
  } catch (error) {
    console.error("Error formatting message:", error);
    // Âá∫ÈîôÊó∂ËøîÂõûÂéüÂßãÂÜÖÂÆπ
    return content;
  }
};

